//! Host-side entry point for the modular Bolt-based prover.
//!
//! `prove_program` takes a guest `Program` + inputs and returns a
//! verifiable `JoltProof` by driving the modular stack
//! (jolt-prover + jolt-verifier + Bolt goldens) end-to-end. The
//! companion `verify_proof` rebuilds verifier inputs from the prover
//! artifacts and closes the soundness loop without re-tracing the
//! guest. Together they form the host-side surface that
//! `#[jolt::provable(backend = "modular")]` lowers to.
//!
//! Mirrors `crates/jolt-equivalence/src/bolt_oracle.rs`'s canonical
//! staged-prove chain with three substitutions:
//!   1. Bolt program plans come from `default_prover_programs()` /
//!      `default_verifier_programs()` (committed `pub const` goldens)
//!      instead of being regenerated by Bolt at runtime.
//!   2. Trace-derived data is built from a real tracer run on the
//!      supplied ELF rather than borrowed from a fixture.
//!   3. The monolithic `prove_jolt_with_witness_inputs` call is
//!      bypassed; we keep the staged artifacts and assemble `JoltProof`
//!      from them directly.

