//! Extract Poseidon constants from light-poseidon for Go code
//!
//! Usage:
//!   cargo run --release --bin extract_poseidon_constants -- 4  # Width-4
//!   cargo run --release --bin extract_poseidon_constants -- 5  # Width-5

use ark_bn254::Fr;
use ark_ff::{BigInt, PrimeField};
use light_poseidon::parameters::bn254_x5::get_poseidon_parameters;

fn bigint_to_decimal(b: &BigInt<4>) -> String {
    let limbs = b.0;
    use num_bigint::BigUint;
    let mut value = BigUint::from(limbs[3]);
    value = (value << 64) + limbs[2];
    value = (value << 64) + limbs[1];
    value = (value << 64) + limbs[0];
    value.to_string()
}

fn fr_to_decimal(f: &Fr) -> String {
    bigint_to_decimal(&f.into_bigint())
}

fn main() {
    let args: Vec<String> = std::env::args().collect();
    let width: u8 = if args.len() > 1 {
        args[1].parse().expect("Width must be a number (4 or 5)")
    } else {
        4 // Default to width-4
    };

    let params = get_poseidon_parameters::<Fr>(width).expect(&format!("width-{} params", width));

    println!("// Width-{} Poseidon constants for BN254 (from light-poseidon circom)", width);
    println!("// 8 full rounds, {} partial rounds, x^5 S-box", params.partial_rounds);
    println!("// Generated by extract_poseidon_constants.rs");
    println!("//");
    println!("// Algorithm (circom-compatible, unoptimized):");
    println!("// 1. state = inputs with domain_tag=0 prepended");
    println!("// 2. For each round in [0, full_rounds + partial_rounds):");
    println!("//    a. ARK: add round constants to ALL state elements");
    println!("//    b. S-box: x^5 on ALL elements (full rounds) or first only (partial rounds)");
    println!("//    c. MDS: multiply by full MDS matrix");
    println!("// 3. Return state[0]");
    println!();
    println!("package poseidon");
    println!();
    println!("import \"math/big\"");
    println!();

    // Print constants
    println!("const BN254_WIDTH{}_FULL_ROUNDS int = 8", width);
    println!("const BN254_WIDTH{}_PARTIAL_ROUNDS int = {}", width, params.partial_rounds);
    println!("const BN254_WIDTH{}_SPONGE_WIDTH int = {}", width, width);
    println!();

    let total_ark = params.ark.len();
    println!("// c{}Constants: round constants (ark) for width-{} Poseidon", width, width);
    println!("// Total: {} elements = ({} full + {} partial rounds) * {} width",
             total_ark, 8, params.partial_rounds, width);
    println!("var c{}Constants []*big.Int", width);
    println!();

    println!("// m{}Matrix: {}x{} MDS matrix for width-{} Poseidon", width, width, width, width);
    println!("var m{}Matrix [][]*big.Int", width);
    println!();

    println!("func initWidth{}Constants() {{", width);
    println!("\tc{}Constants = make([]*big.Int, {})", width, total_ark);

    for (i, c) in params.ark.iter().enumerate() {
        println!("\tc{}Constants[{}], _ = new(big.Int).SetString(\"{}\", 10)", width, i, fr_to_decimal(c));
    }
    println!();

    println!("\tm{}Matrix = make([][]*big.Int, {})", width, width);
    println!("\tfor i := 0; i < {}; i++ {{", width);
    println!("\t\tm{}Matrix[i] = make([]*big.Int, {})", width, width);
    println!("\t}}");

    for (i, row) in params.mds.iter().enumerate() {
        for (j, c) in row.iter().enumerate() {
            println!("\tm{}Matrix[{}][{}], _ = new(big.Int).SetString(\"{}\", 10)", width, i, j, fr_to_decimal(c));
        }
    }
    println!("}}")
}
